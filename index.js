async function handleMessage(senderId, userText, platform) {
  console.log(`📩 [${platform}] від ${senderId}: ${userText}`);

  const now = Date.now();
  const lastTime = lastMessageTime[senderId] || 0;
  const isNewConversation = (now - lastTime) > GREETING_TIMEOUT_MS;

  // Оновлюємо час останнього повідомлення
  lastMessageTime[senderId] = now;

  try {
    const aiReply = await getClaudeResponse(userText, isNewConversation);
    await sendMessage(senderId, aiReply, platform);
    console.log(`✅ Відповідь надіслано: ${aiReply.substring(0, 80)}...`);
  } catch (error) {
    console.error('❌ Помилка:', error.message);
    await sendMessage(senderId, 'Вибачте, виникла технічна помилка. Будь ласка, зв\'яжіться з нами: instituteaba.ua@gmail.com', platform);
  }
}

