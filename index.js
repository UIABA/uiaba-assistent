// CLAUDE API
// ============================================================
async function getClaudeResponse(userMessage, isNewConversation) {
  // Додаємо контекст про вітання до повідомлення
  const contextNote = isNewConversation
    ? '[Це початок нової розмови або людина повернулась після тривалої перерви — привітайся.]'
    : '[Це продовження активного діалогу — НЕ вітайся, одразу відповідай по суті.]';

  const response = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-api-key': CONFIG.ANTHROPIC_API_KEY,
      'anthropic-version': '2023-06-01'
    },
    body: JSON.stringify({
      model: 'claude-sonnet-4-20250514',
      max_tokens: 1000,
      system: SYSTEM_PROMPT,
      messages: [
        {
          role: 'user',
          content: `${contextNote}\n\nПовідомлення від користувача: ${userMessage}`
        }
      ]
    })
  });

  if (!response.ok) {
    const err = await response.text();
    throw new Error(`Claude API error: ${err}`);
  }

  const data = await response.json();
  return data.content[0].text;
}

